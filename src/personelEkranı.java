    public void arabaları_getir(){
        ArrayList<personel> personel_array=new ArrayList<personel>();
        DefaultTableModel model=(DefaultTableModel)tablo_alanı.getModel();
        model.setRowCount(0);
        personel_array=personelislemleri.arabaları_getir();
        if(personel_array!=null){
           for(personel personel:personel_array){
             
               Object [] eklenecekler={personel.getId(),personel.getMarka(),personel.getModel(),personel.getFiyat()};
               model.addRow(eklenecekler);
           
           }
           
        }
    
    }

