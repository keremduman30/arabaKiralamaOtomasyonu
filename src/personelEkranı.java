    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
    mesaj_alanı.setText("");
    String marka=marka_alanı.getText();
    String model=model_alanı.getText();
    String fiyat=fiyat_alanı.getText();
    if(marka.trim().equals("")){
    mesaj_alanı.setText("lütfen marka alanı doldurunuz");
    
    }
    else if(model.trim().equals("")){
        mesaj_alanı.setText("lütfen model alanını bos bırakmayınız");
        
    }
    else if(fiyat.trim().equals("")){
    mesaj_alanı.setText("fiyat alanı bos bırakılamaz");
    }
    else{
    
    personelislemleri.ekle(marka,model,fiyat);
    mesaj_alanı.setText("ekleme basarılı");
    arabaları_getir();
   
    }
        
    }//GEN-LAST:event_ekleActionPerformed

    private void tablo_alanıMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablo_alanıMouseClicked
        DefaultTableModel model=(DefaultTableModel)tablo_alanı.getModel();
        int secilirow=tablo_alanı.getSelectedRow();
        marka_alanı.setText(model.getValueAt(secilirow, 1).toString());
        model_alanı.setText(model.getValueAt(secilirow, 2).toString());
        fiyat_alanı.setText(model.getValueAt(secilirow, 3).toString());
        
    }//GEN-LAST:event_tablo_alanıMouseClicked

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        mesaj_alanı.setText("");
        DefaultTableModel model=(DefaultTableModel)tablo_alanı.getModel();
        int secilirow=tablo_alanı.getSelectedRow();
       if(secilirow==-1){
           if(model.getRowCount()==0){
               mesaj_alanı.setText("tablonuz suan bos");
           
           }
           else{
           mesaj_alanı.setText("lütfen silinecek satırı seçin");
           }
       }
       else{
       int id=(int)model.getValueAt(secilirow, 0);
       personelislemleri.sil(id);
       mesaj_alanı.setText("silme basarılı");
       arabaları_getir();
       
       }
       
        
    }//GEN-LAST:event_silActionPerformed

    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
      DefaultTableModel model1=(DefaultTableModel)tablo_alanı.getModel();
      mesaj_alanı.setText("");
      int secilirow=tablo_alanı.getSelectedRow();
      String marka=marka_alanı.getText();
      String model=model_alanı.getText();
      String fiyat=fiyat_alanı.getText();
    
      if(secilirow==-1){
          if(model1.getRowCount()==0){
          mesaj_alanı.setText("tablonuz suan bos");
          }
          else{
          mesaj_alanı.setText("lütfen güncellenecek satırı secin");
          }
      
      
      }
      else {
      int id=(int)model1.getValueAt(secilirow, 0);
      personelislemleri.güncelle(id,marka,model,fiyat);
      mesaj_alanı.setText("guncelleme basarılı");
      arabaları_getir();
      marka_alanı.setText("");
      model_alanı.setText("");
      fiyat_alanı.setText("");
      }
      
        
    }//GEN-LAST:event_guncelleActionPerformed

