
public class giris {
private String ad;
private String parola;

    public giris(String ad, String parola) {
        this.ad = ad;
        this.parola = parola;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getParola() {
        return parola;
    }

    public void setParola(String parola) {
        this.parola = parola;
    }

}
